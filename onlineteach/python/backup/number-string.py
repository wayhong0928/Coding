# 數字運算

# 次方

x=3**6 
print(x)

#開根號

x=4**0.5    
print(x)

# 整數運算

x=7//6
print(x)

# MOD運算 取餘數

x=7%3
print(x)    

# 字串運算

s="hello"
print(s)

# 字串跳脫

s="hell\"o"
print(s)

# 串接

s="he" + "llo"
print(s)

s="a"  "b"
print(s)

# 換行

s="hello\world"
print(s)

s="""hello
world"""
print(s)

# 重複輸出

s="hi"*3 +"word"
print(s)

# 字串會對內部的字元編號(索引)，從0開始算

s="hello"
print(s[0])
print(s[1:4])
print(s[2:])
print(s[:4])
